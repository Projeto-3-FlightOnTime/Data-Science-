from sklearn.base import BaseEstimator, ClassifierMixin
import numpy as np

class ModeloComThreshold(BaseEstimator, ClassifierMixin):
    def __init__(self, modelo, threshold=0.4):
        self.modelo = modelo
        self.threshold = threshold

        if hasattr(modelo, "classes_"):
            self.classes_ = modelo.classes_

    def predict(self, X):
        if not hasattr(self.modelo, "predict_proba"):
            raise AttributeError("O modelo interno nÃ£o suporta predict_proba")

        prob_atraso = self.modelo.predict_proba(X)[:, 1]
        return (prob_atraso >= self.threshold).astype(int)

    def predict_proba(self, X):
        return self.modelo.predict_proba(X)
